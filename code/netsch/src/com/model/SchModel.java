package com.model;

public class SchModel {
	private String user_id, user_name, avail_day, start_time, end_time, schd_seq;

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getAvail_day() {
		return avail_day;
	}

	public void setAvail_day(String avail_day) {
		this.avail_day = avail_day;
	}

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public String getEnd_time() {
		return end_time;
	}

	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}

	public String getSchd_seq() {
		return schd_seq;
	}

	public void setSchd_seq(String schd_seq) {
		this.schd_seq = schd_seq;
	}

	
}
